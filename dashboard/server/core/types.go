// Package core contains the canonical business logic for pokegents:
// state machine, session identity resolution, and shared types.
package core

// SessionState represents the state of an agent in the state machine.
type SessionState string

const (
	StateIdle       SessionState = "idle"
	StateBusy       SessionState = "busy"
	StateNeedsInput SessionState = "needs_input"
	StateError      SessionState = "error"
)

// IsActive returns true if the agent is doing work or waiting for input.
func (s SessionState) IsActive() bool {
	return s == StateBusy || s == StateNeedsInput
}

// CanInterrupt returns true if a tool event (PreToolUse/PostToolUse) is
// allowed to set the state. Only busy and needs_input can be overwritten
// by tool events — error/idle require UserPromptSubmit to transition.
func (s SessionState) CanInterrupt() bool {
	return s == StateBusy || s == StateNeedsInput || s == ""
}

// HookEvent is the JSON payload from Claude Code lifecycle hooks.
// Received via HTTP POST from dashboard-notify.sh or via the hook input pipe.
type HookEvent struct {
	SessionID            string `json:"session_id"`
	HookEventName        string `json:"hook_event_name"`
	ToolName             string `json:"tool_name,omitempty"`
	ToolInput            any    `json:"tool_input,omitempty"`
	CWD                  string `json:"cwd"`
	LastAssistantMessage string `json:"last_assistant_message,omitempty"`
	NotificationType     string `json:"notification_type,omitempty"`
	TranscriptPath       string `json:"transcript_path,omitempty"`
	Prompt               string `json:"prompt,omitempty"`
}

// SessionIdentity holds the multiple IDs that identify an agent session.
// See CLAUDE.md "Session Identity" for why there are three IDs.
type SessionIdentity struct {
	// SessionID is Claude's conversation session ID (assigned by Claude, may change on resume).
	SessionID string
	// RunID is the stable unique agent ID generated by boa.sh.
	RunID string
	// ProfileName is the profile this session was launched from.
	ProfileName string
	// TTY is the terminal device (e.g. /dev/ttys001).
	TTY string
	// ITermSessionID is the iTerm2 session UUID (stable across tab moves).
	ITermSessionID string
}

// --- Data types (shared across store, services, and server) ---

// AgentIdentity is the persistent identity for an agent, stored in ~/.the-binding-of-agents/agents/{pokegent_id}.json.
// Unlike RunningSession (ephemeral, deleted on exit), identity files are never deleted automatically.
type AgentIdentity struct {
	RunID       string `json:"run_id"`
	DisplayName string `json:"display_name"`
	Sprite      string `json:"sprite,omitempty"`
	Role        string `json:"role,omitempty"`
	Project     string `json:"project,omitempty"`
	Profile     string `json:"profile,omitempty"`
	TaskGroup   string `json:"task_group,omitempty"`
	Model       string `json:"model,omitempty"`
	Effort      string `json:"effort,omitempty"`
	CreatedAt   string `json:"created_at,omitempty"`
	// Interface is the runtime backend bound to this agent: "" / "iterm2" =
	// Claude Code in a terminal tab; "chat" = ACP-backed embedded panel.
	// Set at launch time, survives session resume.
	Interface string `json:"interface,omitempty"`
	// AgentBackend is the configured chat backend key, e.g. "claude" or
	// "codex". It is needed to revive chat agents from the PC box.
	AgentBackend string `json:"agent_backend,omitempty"`
}

// RunningSession is the data stored in ~/.the-binding-of-agents/running/*.json.
type RunningSession struct {
	Profile        string `json:"profile"`
	SessionID      string `json:"session_id"`
	PID            int    `json:"pid"`
	ClaudePID      int    `json:"claude_pid"`
	TTY            string `json:"tty"`
	DisplayName    string `json:"display_name"`
	RunID          string `json:"run_id,omitempty"`
	ITermSessionID string `json:"iterm_session_id,omitempty"`
	CreatedAt      string `json:"created_at,omitempty"`
	Role           string `json:"role,omitempty"`            // role name (empty for legacy/bare project)
	Project        string `json:"project,omitempty"`         // project name (empty for legacy profiles)
	TaskGroup      string `json:"task_group,omitempty"`      // organizational grouping (e.g. "proxy", "auth-migration")
	Model          string `json:"model,omitempty"`           // provider model id (legacy aliases accepted on migration)
	Effort         string `json:"effort,omitempty"`          // reasoning effort level (low, medium, high, max)
	Sprite         string `json:"sprite,omitempty"`          // avatar sprite name (single source of truth)
	Interface      string `json:"interface,omitempty"`       // "" / "iterm2" or "chat" — runtime backend
	AgentBackend   string `json:"agent_backend,omitempty"`   // ACP backend type: "claude-acp", "codex-acp", "codex"
	CWD            string `json:"cwd,omitempty"`             // working directory (persisted for non-Claude reattach)
	TranscriptPath string `json:"transcript_path,omitempty"` // explicit transcript path (for non-Claude backends)
	// LastGood* preserves the most recent verified native transcript/session
	// pair. Chat ACP restarts may produce a transient session_id before a
	// transcript exists; revive/reattach should fall back to this pair instead
	// of overwriting durable history with an unverified backend session.
	LastGoodSessionID      string `json:"last_good_session_id,omitempty"`
	LastGoodTranscriptPath string `json:"last_good_transcript_path,omitempty"`
	// SourceTranscriptPath preserves the pre-migration transcript when a chat
	// agent switches backends (for example Claude → Codex). Non-Claude ACP
	// backends cannot load Claude's session directly, so relaunch uses this
	// path to inject prior conversation context into the new backend.
	SourceTranscriptPath string `json:"source_transcript_path,omitempty"`
}

