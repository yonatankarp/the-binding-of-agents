// --- State machine result ---

// StateTransitionResult is returned by ApplyEvent with the new state and metadata.
type StateTransitionResult struct {
	// NewState is the resulting state after the event.
	NewState SessionState
	// Skip is true if the event should be ignored (e.g. clone SessionStart on busy agent).
	Skip bool
	// Detail is the human-readable status detail.
	Detail string
	// Summary is the last assistant message (set on Stop).
	Summary string
	// BusySince is the timestamp when the agent became busy (set on UserPromptSubmit).
	BusySince string
	// ClearActions is true if recent_actions should be reset.
	ClearActions bool
	// ToolAction is the action string to append to recent_actions (if non-empty).
	ToolAction string
}
